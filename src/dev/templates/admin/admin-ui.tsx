/* Smooth monochrome area chart (Catmull-Rom → Bézier). */
export function AreaChart({ data, className }: { data: number[]; className?: string }) {
  const W = 640;
  const H = 200;
  const pad = 6;
  const n = data.length;
  const x = (i: number) => (i / (n - 1)) * (W - pad * 2) + pad;
  const y = (v: number) => H - pad - v * (H - pad * 2);
  const pts = data.map((v, i) => [x(i), y(v)] as const);

  let line = `M ${pts[0][0]} ${pts[0][1]}`;
  for (let i = 0; i < n - 1; i++) {
    const p0 = pts[i - 1] ?? pts[i];
    const p1 = pts[i];
    const p2 = pts[i + 1];
    const p3 = pts[i + 2] ?? p2;
    const c1x = p1[0] + (p2[0] - p0[0]) / 6;
    const c1y = p1[1] + (p2[1] - p0[1]) / 6;
    const c2x = p2[0] - (p3[0] - p1[0]) / 6;
    const c2y = p2[1] - (p3[1] - p1[1]) / 6;
    line += ` C ${c1x} ${c1y}, ${c2x} ${c2y}, ${p2[0]} ${p2[1]}`;
  }
  const area = `${line} L ${x(n - 1)} ${H} L ${x(0)} ${H} Z`;

  return (
    <svg viewBox={`0 0 ${W} ${H}`} preserveAspectRatio="none" className={className} aria-hidden="true">
      <defs>
        <linearGradient id="adminArea" x1="0" y1="0" x2="0" y2="1">
          <stop offset="0%" stopColor="rgb(var(--fg-rgb))" stopOpacity="0.24" />
          <stop offset="100%" stopColor="rgb(var(--fg-rgb))" stopOpacity="0" />
        </linearGradient>
      </defs>
      {[0.25, 0.5, 0.75].map((g) => (
        <line key={g} x1={pad} x2={W - pad} y1={H - pad - g * (H - pad * 2)} y2={H - pad - g * (H - pad * 2)} stroke="rgb(var(--fg-rgb))" strokeOpacity="0.08" strokeDasharray="3 4" strokeWidth="1" />
      ))}
      <path d={area} fill="url(#adminArea)" />
      <path d={line} fill="none" stroke="rgb(var(--fg-rgb))" strokeOpacity="0.8" strokeWidth="2" vectorEffect="non-scaling-stroke" strokeLinecap="round" strokeLinejoin="round" />
    </svg>
  );
}
